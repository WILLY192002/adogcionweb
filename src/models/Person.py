class Person:
    def __init__(self, id, first_name, middle_name, first_surname, second_lastname, identification_type, identification_number, contact, city, department):
        self.id = id
        self.first_name = first_name
        self.middle_name = middle_name
        self.first_surname = first_surname
        self.second_lastname = second_lastname
        self.identification_type = identification_type
        self.identification_number = identification_number
        self.contact = contact
        self.city = city
        self.department = department
